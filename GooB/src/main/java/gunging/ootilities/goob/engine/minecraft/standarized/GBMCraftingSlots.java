package gunging.ootilities.goob.engine.minecraft.standarized;

/**
 * Ordinals for the default crafting slots, it might
 * be possible to somehow have an expanded crafting
 * menu in modded mc but in that case just use indices
 * as usual instead of this enum.
 * 
 * @author Gunging
 * @since 1.0.0
 */
public enum GBMCraftingSlots {

    /**
     * Literally index zero, the result slot
     *
     * @since 1.0.0
     */
    RESULT,
    
    /**
     * Index 1
     *
     * @since 1.0.0
     */
    TOP_LEFT,
    
    /**
     * Index 2
     *
     * @since 1.0.0
     */
    TOP_RIGHT,
    
    /**
     * Index 3
     *
     * @since 1.0.0
     */
    BOTTOM_LEFT,
    
    /**
     * Index 4
     *
     * @since 1.0.0
     */
    BOTTOM_RIGHT
}
